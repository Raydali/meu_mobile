// A primeira e mais comum instrução para exibir algo no console
console.log("Olá, mundo! Este é o primeiro arquivo.");

// A instrução alert() exibe uma caixa de diálogo no navegador
// alert("Bem-vindo à revisão de JS!");

// Variáveis: Contêineres para armazenar dados
// Use 'let' para variáveis que podem ser reatribuídas
let nome = "João";
console.log("Meu nome é " + nome);

// Use 'const' para variáveis que não podem ser reatribuídas (constantes)
const idade = 30;
console.log("Minha idade é " + idade);

// Tipos de dados primitivos
// String: texto
const saudacao = "Olá!";

// Number: números inteiros ou decimais
const numeroInteiro = 10;
const numeroDecimal = 10.5;

// Boolean: verdadeiro ou falso
const estaChovendo = true;

// Null: representa a ausência intencional de qualquer valor
let valorNulo = null;

// Undefined: o valor de uma variável que foi declarada, mas não recebeu um valor
let valorIndefinido;
console.log("Valor indefinido:", valorIndefinido);